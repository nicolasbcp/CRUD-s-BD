    private void ButtonCadastrarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarAlunoActionPerformed
        Aluno aluno = new Aluno();
        String nome,matricula,curso;
        try{
            nome = TextFieldNomeAluno.getText();
            curso = TextFieldCursoAluno.getText();
            matricula = TextFieldMatricula.getText();

            aluno.setNome(nome);
            aluno.setMatricula(matricula);
            aluno.setCurso(curso);

            alunosCadastrados.add("Aluno: "+aluno.getNome()+"\nMatrícula: "+aluno.getMatricula()+"\nCurso: "+aluno.getCurso()+"\n\n");

            JOptionPane.showMessageDialog(null,"Aluno(a) cadastrado com sucesso!");
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Falhou");
        }

        TextFieldNomeAluno.setText("");
        TextFieldCpfAluno.setText("");
        TextFieldMatricula.setText("");
        TextFieldCursoAluno.setText("");
        jSpinner3.setValue(1900);
    }//GEN-LAST:event_ButtonCadastrarAlunoActionPerformed

